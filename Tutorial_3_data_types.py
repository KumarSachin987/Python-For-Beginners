# Integer data type
a = 756
print('value of a is=',a)
print('type of a is=',type(a))

# float data type
y = 3.14
print('value of y is=',y)
print('type of y is=',type(y))

# complex data type
z = 2 + 3j
print('value of z is=',z)
print('type of z is=',type(z))

# String data type
s = "Hello"
print('value of s is=',s)
print('type of s is=',type(s))

# list data type
my_list = [1, 2, 3,'r','e',5.6]
print('value of my_list is=',my_list)
print('type of my_list is=',type(my_list))

# tuple data type
my_tuple = (1, 2, 3,5.6,'r')
print('value of my_tuple is=',my_tuple)
print('type of my_tuple is=',type(my_tuple))

# set data type
my_set = {1, 2, 3,3,4,4,5,5,1,2,3}
print('value of my_set is=',my_set)
print('type of my_set is=',type(my_set))

# dictionary data type
my_dict = {'a': 1, 'b': 2}
print('value of my_dict is=',my_dict)
print('type of my_dict is=',type(my_dict))

# boolean data type
is_true = True
print('value of is_true is=',is_true)
print('type of is_true is=',type(is_true))

is_false = False
print('value of is_false is=',is_false)
print('type of is_false is=',type(is_false))

# None data type
none_type = None
print('value of none_type is=',none_type)
print('type of none_type is=',type(none_type))
'''
In Python, data types represent the type or category of data that a variable can store. Python is dynamically typed, which means you don't have to explicitly declare the data type of a variable; the interpreter infers it based on the value assigned to the variable. Here are some common data types in Python:

Numeric Types:
int: Integer type, e.g., x = 5
float: Floating-point type, e.g., y = 3.14
complex: Complex number type, e.g., z = 2 + 3j

Sequence Types:
str: String type, e.g., s = "Hello"
list: List type, e.g., my_list = [1, 2, 3]
tuple: Tuple type, e.g., my_tuple = (1, 2, 3)

Set Types:
set: Unordered collection of unique elements, e.g., my_set = {1, 2, 3}

Mapping Type:
dict: Dictionary type, e.g., my_dict = {'a': 1, 'b': 2}

Boolean Type:
bool: Boolean type, representing True or False, e.g., is_true = True

None Type:
None: Represents the absence of a value or a null value.
'''
