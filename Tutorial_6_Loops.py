# for loop
for i in range(1,31):
    print(i)

# iterating over sequence using for loop
lst = [1,2,3,4,5,6]
for data in lst:
    print(data)

# while loop
count = 1
while count < 31:
    print(count)
    count = count + 3  # 30+1 = 31
'''
In Python, loops are used to repeatedly execute a block of code as long as a certain condition is true. There are two main types of loops in Python: for loops and while loops.

1). for loop:-
A for loop is used for iterating over a sequence (that is either a list, a tuple, a dictionary, a string, or other iterable objects).

The basic syntax of a for loop is as follows:

for variable in iterable:
    # code to be executed


2). while loop:-
A while loop is used to repeatedly execute a block of code as long as the given condition is true.
The basic syntax of a while loop is as follows:

while condition:
    # code to be executed

'''